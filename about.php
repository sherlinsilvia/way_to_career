<h1>HISTORY OF ENGINEERING</h1>
<p>Engineering has existed since ancient times, when humans devised inventions such as the wedge, lever, wheel and pulley, etc.
The term engineering is derived from the word engineer, which itself dates back to the 14th century when an engine'er 
(literally, one who builds or operates a siege engine) referred to "a constructor of military engines".[7] In this context,
 now obsolete, an "engine" referred to a military machine, i.e., a mechanical contraption used in war 
 (for example, a catapult). Notable examples of the obsolete usage which have survived to the present 
 day are military engineering corps, e.g., the U.S. Army Corps of Engineers.
The word "engine" itself is of even older origin, ultimately deriving from the Latin ingenium (c.1250), 
meaning "innate quality, especially mental power, hence a clever invention."[8]
Later, as the design of civilian structures, such as bridges and buildings,
 matured as a technical discipline, the term civil engineering[6] entered the lexicon as a way
to distinguish between those specializing in the construction of such non-military projects and those involved in the
discipline of military engineering.</p>
