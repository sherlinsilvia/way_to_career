<h3>CHEMICAL ENGINEERING</h3>
<p>Chemical engineering is the application of physics, chemistry, biology, and engineering principles in order to carry out 
chemical processes on a commercial scale, such as the manufacture of commodity chemicals, specialty chemicals, 
petroleum refining, microfabrication, fermentation, and biomolecule production.
<h3>Civil engineering</h3>
<p>Civil engineering is the design and construction of public and private works, 
    such as infrastructure (airports, roads, railways, water supply, and treatment etc.),
     bridges, tunnels, dams, and buildings.[59][60] Civil engineering is traditionally broken into a
      number of sub-disciplines, including structural engineering, environmental engineering, and surveying.
       It is traditionally considered to be separate from military engineering.[61]</p>
